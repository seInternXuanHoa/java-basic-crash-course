package gpcoder.java.basic.ifelse;

public class IfElseIST {
	public static void main(String[] args) {
		int gender = 0;

		if (gender == 0) {
			System.out.println("Hi! Mr!");
		} else if (gender == 1) {
			System.out.println("Hi! Mrs!");
		} else if (gender == 2) {
			System.out.println("Hi!");
		} else {
			System.out.println("Invalid Option!");
		}
	}
}
